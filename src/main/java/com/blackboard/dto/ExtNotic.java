package com.blackboard.dto;

public class ExtNotic {

	private String extensionType;
	private String extensionVer;
	private ExtensionInfo extensionInfo;

	public String getExtensionType() {
		return extensionType;
	}

	public void setExtensionType(String extensionType) {
		this.extensionType = extensionType;
	}

	public String getExtensionVer() {
		return extensionVer;
	}

	public void setExtensionVer(String extensionVer) {
		this.extensionVer = extensionVer;
	}

	public ExtensionInfo getExtensionInfo() {
		return extensionInfo;
	}

	public void setExtensionInfo(ExtensionInfo extensionInfo) {
		this.extensionInfo = extensionInfo;
	}

}
