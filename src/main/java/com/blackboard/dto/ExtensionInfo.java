package com.blackboard.dto;

public class ExtensionInfo {

	private String forwardable;
	private String accessNo;
	private String displayMode;
	private ActMsg actMsg;

	public String getForwardable() {
		return forwardable;
	}

	public void setForwardable(String forwardable) {
		this.forwardable = forwardable;
	}

	public String getAccessNo() {
		return accessNo;
	}

	public void setAccessNo(String accessNo) {
		this.accessNo = accessNo;
	}

	public String getDisplayMode() {
		return displayMode;
	}

	public void setDisplayMode(String displayMode) {
		this.displayMode = displayMode;
	}

	public ActMsg getActMsg() {
		return actMsg;
	}

	public void setActMsg(ActMsg actMsg) {
		this.actMsg = actMsg;
	}

}
